    private void jbtnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNewActionPerformed
        String name = JOptionPane.showInputDialog(this, "Nombre del projecto:");
        if(name != null) {
            JFileChooser chooser = new JFileChooser();
            chooser.setCurrentDirectory(new java.io.File("."));
            chooser.setDialogTitle("Selecciona el Directorio");
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            chooser.setAcceptAllFileFilterUsed(false);

            if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { 
                try {
                    final String dir = chooser.getSelectedFile().toString();                    
                    for (Model model : listProject) {
                        if(model.getDir().equals(dir)) {
                            JOptionPane.showMessageDialog(this, "Ya existe el Projecto");
                            return ;
                        }
                    }
                    
                    DataBase.DataBaseManager dataBaseManager = new DataBaseManager();
                    dataBaseManager.setProject(name, dir);
                    dispose();
                    openMainView(dir);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(InitView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(InitView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jbtnNewActionPerformed

    private void jlistProjectsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlistProjectsMouseClicked
        JList list = (JList)evt.getSource();
        if (evt.getClickCount() == 2) {
            String dir = "";
            char[] cs = ((String) jlistProjects.getSelectedValue()).toCharArray();
            boolean flag = false;
            for (char c : cs) {
                if(flag)
                    dir += c;
                
                if(c == '|')
                    flag = true;
            }
            dir = dir.replace(" ", "");
            openMainView(dir);
        }
    }//GEN-LAST:event_jlistProjectsMouseClicked

    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        try {
            String dir = "";
            char[] cs = ((String) jlistProjects.getSelectedValue()).toCharArray();
            boolean flag = false;
            for (char c : cs) {
                if(flag)
                    dir += c;

                if(c == '|')
                    flag = true;
            }
            dir = dir.replace(" ", "");  
            System.out.println(dir);
            DataBaseManager baseManager = new DataBaseManager();
            baseManager.removeProject(dir);
            upData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

