    private void openMainView(final String dir) {
        dispose();
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                MainView mainView = new MainView();
                mainView.setDir(dir);
                mainView.setVisible(true);
            }
        });
    }

    private void upData() {
        try {
            DataBase.DataBaseManager baseManager = new DataBaseManager();
            listProject = baseManager.getList();
            
            DefaultListModel defaultListModel = new DefaultListModel();
            for (Model model : listProject) {
                defaultListModel.addElement(model.getName()+" | "+model.getDir());
            }
            jlistProjects.setModel(defaultListModel);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(InitView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(InitView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
